 
package Problem2;
 
public class Kazanc {
   
    
    private int asci;
    private int garson;
    private int masa;
    private int kazanc;

    public Kazanc(int asci, int garson, int masa, int kazanc) {
        this.asci = asci;
        this.garson = garson;
        this.masa = masa;
        this.kazanc = kazanc;
    }

    public int getAsci() {
        return asci;
    }

    public void setAsci(int asci) {
        this.asci = asci;
    }

    public int getGarson() {
        return garson;
    }

    public void setGarson(int garson) {
        this.garson = garson;
    }

    public int getMasa() {
        return masa;
    }

    public void setMasa(int masa) {
        this.masa = masa;
    }

    public int getKazanc() {
        return kazanc;
    }

    public void setKazanc(int kazanc) {
        this.kazanc = kazanc;
    }

}